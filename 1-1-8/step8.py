num = int(input("Enter your grade percentage -> "))
gradeA = 90
gradeB = 80
gradeC = 70
gradeD = 60

if (num >= gradeA):
  print("Your grade is A")
elif (num >= gradeB):
  print("Your grade is B")
elif (num >= gradeC):
  print("Your grade is C")
elif (num >= gradeD):
  print("Your grade is D")
else:
  print("Your grade is F")